# variable = a container for a value. Behaves as the value that it contains

#string data type
name = "Malay"
print("Hello " +name)
print(type(name))


#int data type
age = 20
# age = age + 1
age += 1
print("Your age is:", age)
print(type(age))


#float data type
height = 250.5
print("Your height is:" + str(height) + "cm")
print(type(height))


#boolean data type
human = True
print("Are you a human:" + str(human))
print(type(human))